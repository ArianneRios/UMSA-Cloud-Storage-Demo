// This function calculates stats from stored files
// Used as default when no files are in localStorage yet
export function calculateMockStats(): DashboardStats {
  return {
    total_files: mockFiles.length,
    clean_files: mockFiles.filter((f) => f.status === 'CLEAN').length,
    quarantined_files: mockFiles.filter((f) => f.status === 'QUARANTINED').length,
    analyzing_files: mockFiles.filter((f) => f.status === 'ANALYZING' || f.status === 'UPLOADED').length,
    error_files: mockFiles.filter((f) => f.status === 'ERROR').length,
    storage_used: mockFiles.reduce((sum, f) => sum + f.size, 0),
    registered_students: mockStudents.filter((s) => s.status === 'ACTIVE').length,
  };
}
